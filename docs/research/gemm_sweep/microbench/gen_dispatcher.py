"""Generate main_dispatch.hpp from configs_generated.hpp struct list.

Run from the repo root:
    .venv/bin/python docs/research/gemm_sweep/microbench/gen_dispatcher.py
"""
import re
from pathlib import Path


def main():
    src = (Path(__file__).parent / "configs_generated.hpp").read_text()
    names = re.findall(r"struct (Cfg_\S+) \{", src)

    branches = []
    for n in names:
        branches.append(
            f'    if (s == "{n}") return run_one<GemmFactory<{n}, '
            f'typename {n}::KernelSchedule, '
            f'typename {n}::EpilogueSchedule, '
            f'typename {n}::TileScheduler>>(M, N, K, warmup, timed);'
        )
    body = "\n".join(branches)

    out = Path(__file__).parent / "main_dispatch.hpp"
    out.write_text(
        "#pragma once\n"
        "// Auto-generated by gen_dispatcher.py — do not edit by hand.\n"
        "#include <string>\n\n"
        "inline double dispatch_config(const char* n, int M, int N, int K, int warmup, int timed) {\n"
        "    std::string s(n);\n"
        '    if (s == "smoke_M256") return run_one<SmokeConfig>(M, N, K, warmup, timed);\n'
        f"{body}\n"
        "    return -2.0;  // unknown config\n"
        "}\n"
    )
    print(f"Generated dispatcher for {len(names) + 1} configs (smoke_M256 + {len(names)} generated)")


if __name__ == "__main__":
    main()
